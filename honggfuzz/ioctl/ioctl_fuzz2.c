void
HF_MEMGET(void *dst, size_t len)
{
	size_t diff;

	if (len == 0)
		return;

	pthread_mutex_lock(&mtx);
	do {
		if (buflen == 0) {
			memset(dst, 0, len);
			dst += len;
			len -= len;
			exhausted = true;
		} else {
			diff = MIN(buflen, len);
			memcpy(dst, buf, diff);
			buf += diff;
			buflen -= diff;
			dst += diff;
			len -= diff;
		}
	} while (len > 0);
	pthread_mutex_unlock(&mtx);

#ifdef DEBUG
        FILE *fp = fopen("/tmp/crashlog.txt.x","a+");
        fprintf(fp,"HF_GET: dst=%p len=%zu\n", dst, len);
        fclose(fp);
#endif
}

