static
void Iteration(void)
{
	int fd;

	/* Close all open file descriptors */
	rump_sys_fcntl(0, F_CLOSEM);

	for (fd = 0; fd < 100; fd += 2) {
		int buf[2];
		rump_sys_pipe2(buf, get_pipe2_flags());
	}
	for (fd = 100; fd < 255; fd++) {
		rump_sys_open("/tmp/file", O_RDONLY);
	}
}

int
main(int argc, char **argv)
{
	if (!Initialized) {
		Initialize();
		Initialized=1;
	}

	for (;;) {
        	HF_ITER(&buf, &buflen);

#ifdef DEBUG
		{
	        FILE *fp = fopen("/tmp/crashlog.txt.1","a+");
        	fprintf(fp,"HF_ITER:pid=%d:count=%" PRIu64 ":buflen=%zu\n", getpid(), count, buflen);
	        fclose(fp);
		}
#endif

		exhausted = false;

#ifdef DEBUG
		{
	        FILE *fp = fopen("/tmp/crashlog.txt.1","a+");
        	fprintf(fp,"HF_ITER:::pid=%d:count=%" PRIu64 ":buflen=%zu:zeroed=%d\n", getpid(), count, buflen, zeroed ? 1 : 0);
	        fclose(fp);
		}
#endif

		Iteration();

#ifdef DEBUG
		{
	        FILE *fp = fopen("/tmp/crashlog.txt.1","a+");
        	fprintf(fp,"HF_ITER:pid=%d:count=%" PRIu64 ":buflen=%zu:zeroed=%d\n", getpid(), count, buflen, zeroed ? 1 : 0);
	        fclose(fp);
		}
#endif

		while (exhausted == false) {
#ifdef DEBUG
		{
	        FILE *fp = fopen("/tmp/crashlog.txt.1","a+");
        	fprintf(fp,"HF_ITER:pid=%d:count=%" PRIu64 ":buflen=%zu...\n", getpid(), count, buflen);
	        fclose(fp);
		}
#endif

			register_t retval[2];
			uint16_t syscall_number;
			HF_MEMGET(&syscall_number, 2);

			uint16_t syscall_val = syscall_number & 511;
	
                	switch (syscall_val) {
	                case 54: /* ioctl */
				rump_sys_ioctl(get_u8(), get_ioctl_request());
				break;
	                case 90: /* dup2 */
				rump_sys_dup2(get_u8(), get_u8());
				break;
	                case 453:
				{
				int buf[2];
				rump_sys_pipe2(buf, get_pipe2_flags());
				}
				break;
			}
                }
	}
	return 0;
}
